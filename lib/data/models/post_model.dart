// lib/data/models/post_model.dart
import '../../domain/entities/post_entity.dart';
import '../../../core/extensions/string_extensions.dart';

class PostModel extends PostEntity {
  PostModel({
    required String id,
    required String authorId,
    required String authorName,
    required String authorUsername,
    required String authorAvatar,
    required String content,
    required List<String> mediaUrls,
    required DateTime createdAt,
    required int likes,
    required int comments,
    required int shares,
    required int saves,
    required bool isLiked,
    required bool isShared,
    required bool isSaved,
    required bool isFollowing,
    required List<String> mentionedUsers,
    required List<String> hashtags,
  }) : super(
    id: id,
    authorId: authorId,
    authorName: authorName,
    authorUsername: authorUsername,
    authorAvatar: authorAvatar,
    content: content,
    mediaUrls: mediaUrls,
    createdAt: createdAt,
    likes: likes,
    comments: comments,
    shares: shares,
    saves: saves,
    isLiked: isLiked,
    isShared: isShared,
    isSaved: isSaved,
    isFollowing: isFollowing,
    mentionedUsers: mentionedUsers,
    hashtags: hashtags,
  );

  factory PostModel.fromJson(Map<String, dynamic> json) {
    return PostModel(
      id: json['id'] ?? '',
      authorId: json['authorId'] ?? '',
      authorName: json['authorName'] ?? 'Unknown',
      authorUsername: json['authorUsername'] ?? '@user',
      authorAvatar: json['authorAvatar'] ?? '',
      content: json['content'] ?? '',
      mediaUrls: List<String>.from(json['mediaUrls'] ?? []),
      createdAt: DateTime.parse(json['createdAt'] ?? DateTime.now().toIso8601String()),
      likes: json['likes'] ?? 0,
      comments: json['comments'] ?? 0,
      shares: json['shares'] ?? 0,
      saves: json['saves'] ?? 0,
      isLiked: json['isLiked'] ?? false,
      isShared: json['isShared'] ?? false,
      isSaved: json['isSaved'] ?? false,
      isFollowing: json['isFollowing'] ?? false,
      mentionedUsers: json['content']?.extractMentions() ?? [],
      hashtags: json['content']?.extractHashtags() ?? [],
    );
  }

  Map<String, dynamic> toJson() => {
    'id': id,
    'authorId': authorId,
    'authorName': authorName,
    'authorUsername': authorUsername,
    'authorAvatar': authorAvatar,
    'content': content,
    'mediaUrls': mediaUrls,
    'createdAt': createdAt.toIso8601String(),
    'likes': likes,
    'comments': comments,
    'shares': shares,
    'saves': saves,
    'isLiked': isLiked,
    'isShared': isShared,
    'isSaved': isSaved,
    'isFollowing': isFollowing,
  };

  PostModel copyWith({
    String? id,
    String? authorId,
    String? authorName,
    String? authorUsername,
    String? authorAvatar,
    String? content,
    List<String>? mediaUrls,
    DateTime? createdAt,
    int? likes,
    int? comments,
    int? shares,
    int? saves,
    bool? isLiked,
    bool? isShared,
    bool? isSaved,
    bool? isFollowing,
    List<String>? mentionedUsers,
    List<String>? hashtags,
  }) {
    return PostModel(
      id: id ?? this.id,
      authorId: authorId ?? this.authorId,
      authorName: authorName ?? this.authorName,
      authorUsername: authorUsername ?? this.authorUsername,
      authorAvatar: authorAvatar ?? this.authorAvatar,
      content: content ?? this.content,
      mediaUrls: mediaUrls ?? this.mediaUrls,
      createdAt: createdAt ?? this.createdAt,
      likes: likes ?? this.likes,
      comments: comments ?? this.comments,
      shares: shares ?? this.shares,
      saves: saves ?? this.saves,
      isLiked: isLiked ?? this.isLiked,
      isShared: isShared ?? this.isShared,
      isSaved: isSaved ?? this.isSaved,
      isFollowing: isFollowing ?? this.isFollowing,
      mentionedUsers: mentionedUsers ?? this.mentionedUsers,
      hashtags: hashtags ?? this.hashtags,
    );
  }
}
